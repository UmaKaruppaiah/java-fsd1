package Method;

public class Method {
	static void add(int a, int b) {
		System.out.println(a+b);
		System.out.println("addition of number");
	}
	static void sub(int c, int d) {
		System.out.println(c-d);
		System.out.println("sub");
		
		
	}
	public static void main(String[] args) {
		add(8,9);
		sub(8,7);
	}
	

}
